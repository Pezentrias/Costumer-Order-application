    /**
     * @param args the command line arguments
     */
  public void listafrissit(){
      if(model!=null){
          try {
              szemelyek=model.getAllSzemely();
              listlSzemelyek.setListData(szemelyek.toArray());
          } catch (SQLException ex) {
             MainWindow.HibaAblak(parent, ex.toString());
          }
      }
  }

