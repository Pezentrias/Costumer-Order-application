    private void btUjActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUjActionPerformed
       SzemelyAdatokDialog szad = new SzemelyAdatokDialog(parent,null);
       szad.setVisible(true);
       if(szad.isMentes()){
           Szemely menteni = szad.getSzemely();
           try {
               model.addSzemely(menteni);
               listafrissit(); 
           } catch (SQLException ex) {
              MainWindow.HibaAblak(parent, ex.toString());
           }
       }
    }//GEN-LAST:event_btUjActionPerformed

    private void btModositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModositActionPerformed
      Szemely kivalasztott= (Szemely) listlSzemelyek.getSelectedValue();
        
      if(kivalasztott!=null){
          

        SzemelyAdatokDialog szad = new SzemelyAdatokDialog(parent,kivalasztott);
       szad.setVisible(true);
       if(szad.isMentes()){
           Szemely menteni = szad.getSzemely();
           try {
               model.updateSzemely(menteni);
               listafrissit(); 
           } catch (SQLException ex) {
              MainWindow.HibaAblak(parent, ex.toString());
           }
       }
             }
      else{
          MainWindow.HibaAblak(parent, "To edit a costumer, choose  acostumer!");
      }
    }//GEN-LAST:event_btModositActionPerformed

    private void btTorolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTorolActionPerformed
      Szemely kivalasztott= (Szemely) listlSzemelyek.getSelectedValue();
      if(kivalasztott!=null){
          try {
              if(model.getAllRendeles(kivalasztott).size()==0){
                 int valasz=  JOptionPane.showConfirmDialog(parent, "Are you sure you want to delete the costumer?","Delete costumer",JOptionPane.YES_NO_OPTION);
              if(valasz==JOptionPane.YES_OPTION) {
                 
                      
                      model.removeSzemely(kivalasztott);
                      listafrissit();
              }}else{
                    MainWindow.HibaAblak(parent, kivalasztott.getNev()+" has orders, please delete these orders first!");
              }
                  } catch (SQLException ex) {
                      MainWindow.HibaAblak(parent, ex.toString());
                 
              }
              
}else{
          MainWindow.HibaAblak(parent, "To delete a costumer, choose a costumer!");
      }
    
      
    }//GEN-LAST:event_btTorolActionPerformed

